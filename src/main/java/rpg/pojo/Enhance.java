package rpg.pojo;

/**
 * 强化相关类
 * @author ljq
 *
 */
public class Enhance {
	private int id;
	private int succesRate;
	private int money;
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the succesRate
	 */
	public int getSuccesRate() {
		return succesRate;
	}
	/**
	 * @param succesRate the succesRate to set
	 */
	public void setSuccesRate(int succesRate) {
		this.succesRate = succesRate;
	}
	/**
	 * @return the money
	 */
	public int getMoney() {
		return money;
	}
	/**
	 * @param money the money to set
	 */
	public void setMoney(int money) {
		this.money = money;
	}
}
