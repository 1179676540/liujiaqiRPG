package rpg.pojo;

public class Zb {
	private int id;
	private String name;
	private int ack;
	private int price;
	private int njd;
	/**
	 * @return the njd
	 */
	public int getNjd() {
		return njd;
	}
	/**
	 * @param njd the njd to set
	 */
	public void setNjd(int njd) {
		this.njd = njd;
	}
	/**
	 * @return the price
	 */
	public int getPrice() {
		return price;
	}
	/**
	 * @param price the price to set
	 */
	public void setPrice(int price) {
		this.price = price;
	}
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the ack
	 */
	public int getAck() {
		return ack;
	}
	/**
	 * @param ack the ack to set
	 */
	public void setAck(int ack) {
		this.ack = ack;
	}
	
}
